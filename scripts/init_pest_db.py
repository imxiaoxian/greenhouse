def main():
    searcher = PestSearcher()
    searcher.init()
    print(f"Schema 初始化完成: {config.PEST_DISEASE_DB_FILE}")
    print()

    count = 0
    for crop, name, category, symptoms, cause, treatment_text, prevention_text in PEST_DATA:
        from greenhouse_agent.knowledge.models import Treatment, Prevention

        disease_id = searcher.add_disease(
            crop_type=crop,
            disease_name=name,
            disease_category=category,
            symptoms=symptoms,
            cause=cause,
            treatments=[Treatment(
                drug_name=treatment_text[:50] + "..." if len(treatment_text) > 50 else treatment_text,
                drug_type="化学",
                dosage=treatment_text,
                application_method="喷雾",
            )],
            preventions=[Prevention(measure=prevention_text)],
        )
        count += 1
        print(f"  [{count:2d}] {crop} {name} → id={disease_id}")

    print(f"\n共写入 {count} 条病虫害记录（含 BGE 向量索引）")

    # 验证搜索
    print("\n=== 搜索验证 ===")
    result = searcher.search("番茄叶子发黄有轮纹")
    if result.matched and result.disease:
        print(f"命中: {result.disease.disease_name} (相似度: {result.similarity:.3f})")
    else:
        print(f"未命中（最高相似度: {result.similarity:.3f}）")


if __name__ == "__main__":
    from greenhouse_agent import config  # noqa: E402

    main()
